// Operadores aritiméticos

// Multiplicação = *
console.log(5 * 5 + ' é igual a 25?');
// Divisão = /
console.log(5 / 5 + ' é igual a 1?');
// Soma = +
console.log(5 + 5 + ' é igual a 10?');
// Subtração = -
console.log(5 - 5 + ' é igual a 0?');

// Resto da divisão = %
let remainder = 11 % 3;
console.log(`O resto da divisão é: ${remainder}`);
// Incremento = ++
let number = 5;
console.log(++number);
console.log(number++); // caso o sinal de '++' fique depois, o resultado final só aparecerá na próxima linha
console.log(number);
// Decremento = --
number = 10;
console.log(--number);
// Exponencial = **
console.log(3**3);

