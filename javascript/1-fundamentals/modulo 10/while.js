// Estrutura de repetição
// While

let i = 394589458948;
while(i > 10) {
  console.log(i);
  i /= 35;
}

/*
-------------------------------
OBSERVAÇÃO IMPORTANTE: 
-------------------------------
Quando usar o while?
R: Simples, quando não sabemos quando será a parada do loop
No exemplo acima, o número é muito grande, e a operação do loop
torna inviável calculá-la para saber qual será o resultado final.
Nesses casos, o while ajuda bastante.
*/